<?php

use Datainterface\Database;
use Datainterface\Insertion;
use Datainterface\MysqlDynamicTables;
use Datainterface\Selection;
use Datainterface\Updating;
use GlobalsFunctions\Globals;

if(Globals::method() === "POST" && !empty(Globals::post("submit-profile")))
{
    try {
        $data['firstname'] = Globals::post('firstname');
        $data['lastname'] = Globals::post('lastname');
        $data['phone'] = Globals::post('phone');
        $data['address'] = Globals::post('address');
        $data['image'] = Globals::post('profile_image');

        $flag = [];
        $uid = 0;
        if(!empty(Globals::get("user")))
        {
            $uid = Globals::get("user");
            $flag[] = Updating::update("users", $data, ['uid'=>$uid]);
        }else{
            $uid = Insertion::insertRow("users", $data);
        }
        unset($data);
        $birthday = Globals::post("birthday");
        if(!empty($birthday))
        {
            $birthday = str_replace("/", "-", $birthday);
            $d = (new \DateTime($birthday))->format("d M, Y");
            $data['birthday'] = $d;
        }else{
            $data['birthday'] = "";
        }
        $data['country'] = Globals::post("country");
        $data['state'] = Globals::post("state");
        $data['zip'] = Globals::post("zip");
        $data['gender'] = Globals::post("gender");
        $data['uid'] = $uid;

        (new MysqlDynamicTables())->resolver(
            Database::database(),
            array_keys($data),
            [
                "country"=>['varchar(100)'],
                "state"=>['varchar(100)'],
                "zip"=>['int(11)'],
                "gender"=>['varchar(100)'],
                'birthday'=>['varchar(100)'],
                'uid'=>['int(11)']
            ],
            "users_additional"
        );

        if(!empty(Globals::get("user"))) {
            $already = Selection::selectById("users_additional", ['uid'=>$uid]);
            if(!empty($already))
            {
                unset($data['uid']);
                $flag[] = Updating::update("users_additional", $data, ['uid' => $uid]);
            }else{
                $flag[] = !empty(Insertion::insertRow("users_additional", $data));
            }
        }else{
            $flag[] = Insertion::insertRow("users_additional", $data);
        }

        if(in_array(true, $flag))
        {
            Globals::redirect("/users");
        }else{
            Globals::redirect("edit-profile?user=$uid");
        }
        exit;
    }catch (Throwable $e){
        \ErrorLogger\ErrorLogger::log($e);
    }
}

